void delay_us(uint16_t num)
{
  __HAL_TIM_SET_COUNTER(&htim3, 0); // 将装载值计0
  HAL_TIM_Base_Start(&htim3);       // 开始计数
  while (__HAL_TIM_GetCounter(&htim3) < num)
    ;                        //当装载值小于预设值时循环
  HAL_TIM_Base_Stop(&htim3); //结束延时
}

void delay_ms(uint16_t num)
{
  uint32_t i;
  for (i = 0; i < num; i++)
    delay_us(1000);
}
